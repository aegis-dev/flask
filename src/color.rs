#[derive(Copy, Clone, Eq, PartialEq)]
pub struct Color {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Color {
    pub fn new(r: u8, g: u8, b: u8) -> Color {
        Color { r, g , b }
    }

    pub fn from_hex(hex: u32) -> Color {
        Color { r: (hex >> 16 & 0xFF) as u8, g: (hex >> 8 & 0xFF) as u8 , b: (hex & 0xFF) as u8 }
    }
}